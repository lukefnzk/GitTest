// ============================================================
// 01 · 컴포넌트 & JSX
// ------------------------------------------------------------
// [개념] React 화면은 "컴포넌트"라는 작은 함수들의 조립이다.
//   - 컴포넌트 = UI 조각을 돌려주는 함수. 이름은 반드시 대문자로 시작!
//   - JSX = 함수가 돌려주는, HTML처럼 생긴 문법.
//
// [PHP 비교]
//   - PHP: HTML 안에 <?php ... ?>로 "코드를 끼워넣음".
//   - JSX: 반대로 JS 함수 안에 HTML을 넣고, 필요할 때 { }로 JS를 끼워넣음.
//   - PHP에서 header.php / card.php를 include로 재사용하던 것 =
//     React에서는 <Header/> <Card/> 컴포넌트로 재사용.
//
// [web_class 비교] 문자열로 HTML을 손으로 만들던 것을 JSX가 문법으로 대신해준다.
//
// [JSX 3대 규칙]
//   1) 반환값은 "최상위 요소 하나"로 감싼다. 여러 개면 <>...</> (빈 태그 = Fragment)
//   2) class → className,  for → htmlFor  (class/for는 JS 예약어라서)
//   3) { } 안에는 "값(식)"을 넣는다. 변수, 계산, 함수 호출 결과 등.
// ============================================================

// 재사용할 작은 컴포넌트 (PHP의 include 조각 같은 것)
function Badge() {
  return <span className="badge">NEW</span>
}

export default function Lesson01() {
  const name = 'React' // 평범한 JS 변수
  const year = 2026

  return (
    // <>...</> = Fragment. "최상위 요소 하나" 규칙을 지키려 빈 태그로 감쌌다.
    <>
      <h2>01 · 컴포넌트 &amp; JSX</h2>

      {/* { } 안에 변수/계산을 끼워넣는다 (JSX 주석은 이렇게 중괄호로 감싼다) */}
      <p>안녕, {name}! 지금은 {year}년.</p>
      <p>2 + 2 = {2 + 2}</p> {/* → 2 + 2 = 4 */}
      <p>대문자로: {name.toUpperCase()}</p> {/* → 대문자로: REACT */}

      {/* 내가 만든 컴포넌트를 <태그>처럼 쓴다. 몇 번이든 재사용 가능 */}
      <p>
        배지 재사용: <Badge /> <Badge /> <Badge />
      </p>
    </>
  )
}
// → 화면: 제목 + "안녕, React! 지금은 2026년." + "2 + 2 = 4" + 배지 3개
