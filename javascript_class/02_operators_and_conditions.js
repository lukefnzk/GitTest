// ------------------------------------------------------------
// 1) === vs ==  (JS 초보가 가장 많이 데는 함정)
// ------------------------------------------------------------
// ==  : 타입이 다르면 '알아서 변환'해서 비교 (느슨한 비교 — 예측이 어려움)
// === : 타입까지 똑같아야 true (엄격한 비교 — 항상 이걸 써라)
console.log(1 == "1"); // → true   (숫자 1 과 문자 "1" 을 억지로 맞춤 — 위험)
console.log(1 === "1"); // → false  (타입이 다르므로 false — 이게 안전)
console.log(0 == false); // → true   (역시 억지 변환)
console.log(0 === false); // → false

// [Python 비교] Python 의 == 는 이런 억지 변환을 안 한다 (1 == "1" 은 False).
//              JS 는 == 가 위험하니, Python 의 == 처럼 동작하는 === 를 기본으로 써라.

// ------------------------------------------------------------
// 2) truthy / falsy — if 가 '참으로 치는' 값
// ------------------------------------------------------------
// falsy(거짓 취급) 6종:  false, 0, "" (빈 문자열), null, undefined, NaN
// 그 외에는 전부 truthy(참 취급) — 빈 배열 [], 빈 객체 {} 도 참!
if ("") console.log("안 나옴"); // "" 는 falsy → 실행 안 됨
if ("hi") console.log("나옴"); // → 나옴
if ([]) console.log("빈 배열도 참!"); // → 빈 배열도 참!

// [Python 비교] Python 은 빈 리스트 [] / 빈 딕셔너리 {} 가 '거짓'이지만,
//              JS 는 [] / {} 가 '참'이다. (⚠️ 자주 헷갈리는 차이)

// ------------------------------------------------------------
// 3) if / else if / else
// ------------------------------------------------------------
const score = 85;
if (score >= 90) {
  console.log("A");
} else if (score >= 80) {
  // Python 의 elif = JS 의 else if
  console.log("B"); // → B
} else {
  console.log("C");
}

// ------------------------------------------------------------
// 4) 삼항 연산자 (조건 ? 참일때 : 거짓일때)
// ------------------------------------------------------------
const n = 7;
const parity = n % 2 === 0 ? "짝수" : "홀수";
console.log(parity); // → 홀수
// [Python 비교] "짝수" if n % 2 == 0 else "홀수"  ↔  n % 2 === 0 ? "짝수" : "홀수"
// ★ 이 삼항은 나중에 React 의 조건부 렌더링에서 엄청나게 자주 쓴다.

// ------------------------------------------------------------
// 5) && 와 || — 논리 연산이자 '단축 평가'
// ------------------------------------------------------------
console.log(true && "통과"); // → 통과   (앞이 참이면 뒤를 반환)
console.log(false && "통과"); // → false  (앞이 거짓이면 거기서 멈춤)
console.log(null || "기본값"); // → 기본값 (앞이 falsy 면 뒤를 반환 — 기본값 지정에 자주 씀)
// [Python 비교] Python 의 and/or/not = JS 의 &&/||/!  (동작 원리도 비슷)
// ★ && 로 "조건이 참일 때만 화면에 그리기", || 로 "값 없으면 기본값" — React 단골 패턴.
