/*
 * 03. 함수
 * -----------------------------------------------------------
 * 함수를 만드는 방법이 JS 는 여러 가지다. 특히 '화살표 함수(=>)'는
 * React 에서 압도적으로 많이 쓰니 꼭 익혀두자.
 *
 * 실행:  node 03_functions.js
 */

// ------------------------------------------------------------
// 1) 함수 선언식 — Python 의 def 에 가장 가까움
// ------------------------------------------------------------
function add(a, b) {
  return a + b;
}
console.log(add(2, 3)); // → 5
// [Python 비교] def add(a, b): return a + b   ↔   function add(a, b) { return a + b; }

// ------------------------------------------------------------
// 2) 함수 표현식 — 함수를 '값'처럼 변수에 담는다
// ------------------------------------------------------------
// JS 에서 함수는 '값'이다. 숫자처럼 변수에 넣고, 인자로 넘길 수 있다 (일급 객체).
const multiply = function (a, b) {
  return a * b;
};
console.log(multiply(4, 5)); // → 20

// ------------------------------------------------------------
// 3) 화살표 함수 (=>) — 위 표현식의 짧은 버전. React 의 기본 문법
// ------------------------------------------------------------
const sub = (a, b) => {
  return a - b;
};
console.log(sub(10, 4)); // → 6

// 본문이 'return 한 줄'이면 중괄호와 return 도 생략 가능 (한 줄 화살표)
const square = (x) => x * x; // = (x) => { return x * x; }
console.log(square(6)); // → 36
// [Python 비교] Python 의 lambda x: x*x 와 비슷하지만, 화살표 함수는 여러 줄도 된다.

// ↓ '여러 줄'이란? 중괄호 { } 를 쓰면 본문에 여러 문장을 넣을 수 있다는 뜻.
//   중간 변수를 만들고, 조건 분기를 하고, 마지막에 return 으로 값을 돌려준다.
const grade = (score) => {
  const isPass = score >= 60;           // ① 중간 변수를 만들 수 있고
  const label = isPass ? "합격" : "불합격"; // ② 여러 문장을 이어 쓴 뒤
  return `${score}점 → ${label}`;        // ③ 마지막에 return 으로 결과를 돌려준다
};
console.log(grade(75)); // → 75점 → 합격
console.log(grade(40)); // → 40점 → 불합격
// [Python 비교] Python 의 lambda 는 '식' 하나만 가능해서 위처럼 중간 변수를 못 만든다.
//   → 여러 줄이 필요하면 def grade(score): ... 처럼 def 로 풀어써야 한다.
//   즉 JS 의 '한 줄 화살표'는 lambda, '{ } 화살표'는 def 에 각각 대응한다고 보면 된다.

// ------------------------------------------------------------
// 4) 기본 매개변수 / 나머지 매개변수
// ------------------------------------------------------------
const greet = (name = "손님") => `${name}님 환영합니다`; // 기본값
console.log(greet()); // → 손님님 환영합니다
console.log(greet("영희")); // → 영희님 환영합니다
// [Python 비교] def greet(name="손님")  ↔  (name = "손님") => ...

// ...nums : 인자를 몇 개 받을지 모를 때 배열로 모은다 (Python 의 *args)
const sum = (...nums) => nums.reduce((acc, n) => acc + n, 0);
console.log(sum(1, 2, 3, 4)); // → 10
// [Python 비교] def sum(*nums)  ↔  (...nums) => ...   (모으는 개념 동일)

// ------------------------------------------------------------
// 5) 함수를 인자로 넘기기 (04번 배열 메서드의 토대)
// ------------------------------------------------------------
const doTwice = (fn, value) => fn(fn(value));
console.log(doTwice(square, 2)); // → 16  (square(square(2)) = square(4) = 16)
