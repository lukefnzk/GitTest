/*
 * 04. 배열과 배열 메서드
 * -----------------------------------------------------------
 * 배열(Python 의 list)을 다루는 map / filter / reduce 는
 * React 에서 '목록을 화면에 그릴 때' 매일 쓰는 핵심 도구다. 확실히 익히자.
 *
 * 실행:  node 04_array_methods.js
 */

const nums = [1, 2, 3, 4, 5];

// ------------------------------------------------------------
// 1) 기본 — 인덱스, 길이, 마지막 요소
// ------------------------------------------------------------
console.log(nums[0]); // → 1   (0부터 시작 — Python 과 동일)
console.log(nums.length); // → 5   (Python 의 len(nums) 대신 .length)
console.log(nums[nums.length - 1]); // → 5  (마지막 요소)
// [Python 비교] Python 은 nums[-1] 로 마지막을 꺼내지만, JS 의 nums[-1] 은 동작하지 않는다.
//              (최신 문법 nums.at(-1) 은 되지만, 기본은 length - 1 을 기억하자)

// ------------------------------------------------------------
// 2) forEach — 하나씩 순회만 (Python 의 for x in nums)
// ------------------------------------------------------------
nums.forEach((num) => console.log(num * 10)); // → 10, 20, 30, 40, 50

// ------------------------------------------------------------
// 3) ★ map — 각 요소를 '변형'해 '새 배열'을 만든다
// ------------------------------------------------------------
const doubled = nums.map((num) => num * 2);
console.log(doubled); // → [ 2, 4, 6, 8, 10 ]
console.log(nums); // → [ 1, 2, 3, 4, 5 ]  (원본은 안 변함! map 은 새 배열을 반환)
// [Python 비교] [num * 2 for num in nums]  ↔  nums.map(num => num * 2)
// ★★ React 에서 데이터 배열 → 화면 요소 배열로 바꿀 때 바로 이 map 을 쓴다.

// ------------------------------------------------------------
// 4) filter — 조건을 통과한 요소만 골라 새 배열
// ------------------------------------------------------------
const evens = nums.filter((num) => num % 2 === 0);
console.log(evens); // → [ 2, 4 ]
// [Python 비교] [num for num in nums if num % 2 == 0]  ↔  nums.filter(num => num % 2 === 0)

// ------------------------------------------------------------
// 5) reduce — 전체를 하나의 값으로 '접는다' (합계 등)
// ------------------------------------------------------------
// (누적값 acc, 현재값 num) => 새 누적값,   맨 뒤의 0 은 acc 의 시작값
const total = nums.reduce((acc, num) => acc + num, 0);
console.log(total); // → 15
// [Python 비교] sum(nums)  또는  functools.reduce(lambda a, n: a + n, nums, 0)

// ------------------------------------------------------------
// 6) find / some / every — 찾기·검사
// ------------------------------------------------------------
console.log(nums.find((num) => num > 3)); // → 4     (조건에 맞는 '첫' 요소)
console.log(nums.some((num) => num > 4)); // → true  (하나라도 만족? Python 의 any())
console.log(nums.every((num) => num > 0)); // → true  (전부 만족? Python 의 all())

// ------------------------------------------------------------
// 7) 메서드 체이닝 — 이어붙이기 (실무에서 아주 흔함)
// ------------------------------------------------------------
const result = nums
  .filter((num) => num % 2 === 1) // 홀수만  → [1, 3, 5]
  .map((num) => num * num); // 제곱    → [1, 9, 25]
console.log(result); // → [ 1, 9, 25 ]
