# -*- coding: utf-8 -*-
"""
10. 매직 메서드 (= 던더 메서드, dunder method)
07에서 맛본 __str__ 을 본격적으로 넓힌다.

매직 메서드란?
  이름 양쪽에 밑줄 2개가 붙은 특수 메서드 (__init__, __str__, __eq__ ...).
  'dunder' = double underscore(밑줄 2개)의 줄임말.

핵심 한 줄 요약:
  우리가 직접 부르지 않아도, Python 이 특정 상황(+, ==, len(), print ...)에서
  '자동으로' 불러주는 약속된 메서드다. 즉 '내 클래스가 +, == 같은 것에
  어떻게 반응할지'를 내가 정의하는 것.

하나의 Money(돈) 클래스로 대표적인 매직 메서드들을 모아 익힌다.
"""


class Money:
    def __init__(self, won):
        # __init__ : Money(1000) 처럼 객체를 '만들 때' 자동 호출 (이미 써온 매직 메서드)
        self.won = won

    # ----------------------------------------------------------
    # __str__  : print(객체) / str(객체) -> '사용자용' 보기 좋은 글자
    # __repr__ : repr(객체) / 리스트 안에서 보일 때 -> '개발자용' 정확한 표현
    # __str__ 이 없으면 __repr__ 가 대신 쓰인다. 보통 둘 다 정의한다.
    # ----------------------------------------------------------
    def __str__(self):
        return f"{self.won:,}원"            # 1,000원 (천 단위 콤마)

    def __repr__(self):
        return f"Money({self.won})"          # Money(1000)

    # ----------------------------------------------------------
    # __eq__ : 두 객체를 == 로 비교할 때 자동 호출.
    # 정의하지 않으면 '같은 메모리 객체인가'로만 비교해 내용이 같아도 False 가 난다.
    # ----------------------------------------------------------
    def __eq__(self, other):
        return self.won == other.won

    # ----------------------------------------------------------
    # __lt__ : '<' (less than). 이게 있으면 sorted() / min() / max() 가 동작한다.
    # 리스트 정렬은 내부적으로 원소끼리 < 비교를 하기 때문.
    # ----------------------------------------------------------
    def __lt__(self, other):
        return self.won < other.won

    # ----------------------------------------------------------
    # __add__ : '+' 연산. money1 + money2 -> 새 Money 를 돌려준다.
    # 원본을 바꾸지 않고 '새 객체'를 반환하는 게 좋은 습관.
    # ----------------------------------------------------------
    def __add__(self, other):
        return Money(self.won + other.won)

    # ----------------------------------------------------------
    # __len__ : len(객체) 가 불릴 때. 정수를 돌려줘야 한다.
    # 여기선 '자릿수'를 길이로 정의해본다 (예시용 자유 설계).
    # ----------------------------------------------------------
    def __len__(self):
        return len(str(self.won))            # 1000 -> "1000" -> 4


if __name__ == '__main__':
    a = Money(1000)
    b = Money(500)

    print("===== __str__ vs __repr__ =====")
    print(a)               # __str__  -> 1,000원
    print(repr(a))         # __repr__ -> Money(1000)
    print([a, b])          # 리스트 안에서는 __repr__ -> [Money(1000), Money(500)]

    print("\n===== __eq__ : == 비교 =====")
    print(a == Money(1000))   # True  (won 이 같으면 같다고 봄)
    print(a == b)             # False
    # __eq__ 가 없었다면, 내용이 같은 Money(1000) 끼리도 False 가 나온다.

    print("\n===== __lt__ : 정렬 / min / max =====")
    coins = [Money(3000), Money(1000), Money(2000)]
    print(sorted(coins))      # __lt__ 덕분에 정렬됨 -> [Money(1000), Money(2000), Money(3000)]
    print(min(coins))         # Money(1000)
    print(max(coins))         # Money(3000)

    print("\n===== __add__ : + 연산 =====")
    total = a + b
    print(total)              # 1,500원  (새 Money 객체)
    print(type(total).__name__)   # Money

    print("\n===== __len__ : len() =====")
    print(len(a))             # 4  (1000 은 4자리)
    print(len(Money(50)))     # 2  (50 은 2자리)
