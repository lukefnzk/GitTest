// ============================================================
// 02 · 변수 — Rust 는 기본이 "못 바꿈(immutable)"
// ------------------------------------------------------------
// 실행: cargo run --bin 02_variables
//
// [가장 큰 충격 포인트]
//   Python/JS 는 변수 값을 언제든 바꿀 수 있다.
//   Rust 는 기본이 '불변(immutable)' — 바꾸려면 mut 를 명시해야 한다.
//   "실수로 값이 바뀌는 버그"를 컴파일러가 원천 차단하려는 철학.
// ============================================================

fn main() {
    // ---- 1) 기본은 불변(immutable) ----
    let x = 5;
    println!("x = {x}"); // → x = 5
    // x = 6;   // ← 이 줄의 주석을 풀면 컴파일 에러!
    //            "cannot assign twice to immutable variable `x`"
    //            [JS 비교] const 와 비슷하지만, Rust 는 이게 '기본값'이라는 게 다르다.

    // ---- 2) 바꾸고 싶으면 mut ----
    let mut y = 10;
    println!("y = {y}"); // → y = 10
    y = 20; // mut 이 붙었으니 OK
    println!("y = {y}"); // → y = 20
    // [Python/JS 비교] 그냥 바꾸던 것을, Rust 는 "바꿀 거라고 미리 선언(mut)"해야 한다.

    // ---- 3) 타입 추론 & 정적 타입 ----
    let a = 3; // 타입 안 써도 Rust 가 i32(정수)로 추론
    let b: f64 = 3.0; // 명시도 가능 (f64 = 실수)
    println!("a={a}, b={b}"); // → a=3, b=3
    // [비교] Python/JS 는 타입이 실행 중 정해지지만(동적),
    //        Rust 는 컴파일 시점에 타입이 '고정'된다(정적). 그래서 아래가 에러:
    // let mut c = 3;
    // c = "hello";   // ← 정수였던 c 에 문자열? 컴파일 에러 (타입은 못 바꿈)

    // ---- 4) 섀도잉(shadowing) — 같은 이름으로 '새 변수'를 다시 만들기 ----
    let score = 50;
    let score = score + 10; // 새 score 로 '가림'. mut 아님 — 아예 새 변수를 만든 것.
    let score = score * 2; // 또 가림
    println!("score = {score}"); // → score = 120

    // 섀도잉은 타입도 바꿀 수 있다 (이게 mut 와의 결정적 차이):
    let spaces = "   "; // 문자열(공백 3칸)
    let spaces = spaces.len(); // 같은 이름인데 이번엔 숫자(길이). 섀도잉이라 가능.
    println!("공백 개수 = {spaces}"); // → 공백 개수 = 3

    // ---- 5) 상수 const ----
    const MAX: u32 = 100_000; // 항상 대문자, 타입 필수. _ 는 자릿수 구분(값엔 영향 없음).
    println!("MAX = {MAX}"); // → MAX = 100000
}
