// ============================================================
// 04 · 소유권(ownership) — Rust 의 심장, GC 없이 메모리 안전
// ------------------------------------------------------------
// 실행: cargo run --bin 04_ownership
//
// [왜 존재하나]
//   Python/JS 는 가비지 컬렉터(GC)가 안 쓰는 메모리를 알아서 치운다(느릴 수 있음).
//   C/C++ 는 사람이 직접 치운다 (실수하면 크래시·보안버그).
//   Rust 는 제3의 길: "값마다 주인(owner)은 딱 하나" 규칙으로,
//     주인이 사라지는 순간 메모리를 자동 정리한다. GC 도 없고, 사람이 직접 안 해도 된다.
//   그 규칙을 '컴파일 시점에' 검사하는 게 소유권 시스템이다. → 8.5MB Tauri exe 의 비결.
// ============================================================

fn main() {
    // ---- 1) 이동(move): 대입하면 '소유권'이 넘어간다 ----
    let s1 = String::from("안녕"); // 힙에 저장되는 문자열. s1 이 주인.
    let s2 = s1; // s1 의 소유권이 s2 로 '이동(move)'. 이제 s1 은 무효!
    println!("s2 = {s2}"); // → s2 = 안녕   (s2 가 새 주인)

    // println!("s1 = {s1}");   // ← 이 줄의 주석을 풀면 컴파일 에러!
    //   "borrow of moved value: `s1`"
    //   [충격 포인트] Python/JS 라면 s1, s2 둘 다 같은 걸 가리켜 문제없다.
    //     Rust 는 소유권이 s2 로 넘어갔으니 s1 은 더 못 쓴다 (두 주인 금지).
    //   → "이미 넘긴 값을 실수로 또 쓰는 버그"를 컴파일러가 막아준다.

    // ---- 2) 정수 같은 단순 타입은 '복사(copy)'라 이동이 아니다 ----
    let n1 = 5;
    let n2 = n1; // 정수는 그냥 복사됨. n1 도 여전히 유효!
    println!("n1={n1}, n2={n2}"); // → n1=5, n2=5
    //   [이유] 정수는 작고 힙을 안 써서 통째 복사가 싸다 → move 대신 copy.
    //          String 처럼 힙 쓰는 값은 복사가 비싸 move 로 소유권만 넘긴다.

    // ---- 3) 함수에 넘겨도 소유권이 '이동'한다 ----
    let s = String::from("소유권");
    take_ownership(s); // → 함수가 받음: 소유권      (s 의 소유권이 함수 안으로 이동!)
    // println!("{s}");   // ← 주석 풀면 에러! s 는 함수에 넘겨줘서 여기선 무효.

    // ---- 4) 넘긴 값을 계속 쓰려면? → return 으로 돌려받기 (또는 05 의 '빌리기') ----
    let s3 = String::from("돌려줘");
    let s3 = give_back(s3); // 소유권을 넘겼다가 return 으로 되돌려받음(섀도잉으로 재바인딩)
    println!("돌려받음: {s3}"); // → 돌려받음: 돌려줘
    //   매번 이렇게 주고받는 건 번거롭다 → 그래서 05_borrowing 의 '참조(&)'가 나온다.
}

// 소유권을 '먹는' 함수: 매개변수가 주인이 되고, 함수가 끝나면 메모리 정리됨.
fn take_ownership(text: String) {
    println!("함수가 받음: {text}");
} // ← 여기서 text 의 수명 끝 → 메모리 자동 해제 (GC 없이, 사람 손도 없이!)

// 받은 소유권을 return 으로 그대로 돌려주는 함수
fn give_back(text: String) -> String {
    text // 세미콜론 없음 = text 를 반환 (소유권을 호출자에게 되돌림)
}
