// ============================================================
// 05 · 빌리기(borrowing) — & 로 '소유권은 안 넘기고' 값만 참조
// ------------------------------------------------------------
// 실행: cargo run --bin 05_borrowing
//
// [04 의 문제] 함수에 값을 넘기면 소유권이 이동해서, 그 뒤 원래 변수를 못 썼다.
//   매번 return 으로 돌려받는 건 번거롭다.
// [해결] '참조(reference)' = & 를 붙여 '빌려주기'.
//   소유권은 원래 주인이 그대로 갖고, 값을 '보기만' 하도록 빌려준다. 다 쓰면 자동 반납.
// ============================================================

fn main() {
    // ---- 1) & 로 빌려주면 소유권이 안 넘어간다 ----
    let s1 = String::from("안녕하세요");
    let len = calc_len(&s1); // &s1 = "s1 을 빌려준다" (소유권은 s1 이 유지)
    println!("'{s1}' 의 길이는 {len}"); // → '안녕하세요' 의 길이는 15
    //   s1 을 계속 쓸 수 있다! 소유권을 안 넘겼으니까.
    //   (한글은 UTF-8 로 글자당 3바이트 → len 은 '바이트' 길이라 15. 글자 수 아님에 주의)

    // ---- 2) 고치려면 '가변 참조' &mut ----
    let mut s2 = String::from("안녕"); // 원본이 mut 이어야 함
    add_world(&mut s2); // &mut = "고칠 수 있게 빌려준다"
    println!("s2 = {s2}"); // → s2 = 안녕 world!

    // ---- 3) 규칙: 가변 참조(&mut)는 '동시에 하나만' ----
    //   let r1 = &mut s2;
    //   let r2 = &mut s2;   // ← 주석 풀면 컴파일 에러! 같은 값을 동시에 두 곳이 고치면 위험.
    //   println!("{r1} {r2}");
    //   → 이 규칙이 '데이터 경합(data race)'을 컴파일 시점에 원천 차단한다.
    //     (여러 스레드가 같은 값을 동시에 건드리는 버그 — Rust 가 자랑하는 안전성의 핵심)

    println!("\n정리: & 는 '빌려서 보기', &mut 는 '빌려서 고치기', 소유권은 원주인이 유지!");
}

// &str = "문자열을 빌린다(참조)". 소유권을 안 가져오므로 호출자가 계속 쓸 수 있다.
//   (&String 을 넘겨도 Rust 가 자동으로 &str 로 맞춰준다 — deref 강제변환)
fn calc_len(text: &str) -> usize {
    text.len() // 길이(바이트)를 반환. usize = 크기·인덱스에 쓰는 정수 타입.
} // ← 함수가 끝나도 text 는 '빌린 것'이라 메모리 해제 안 함 (주인은 그대로 s1)

// &mut String = "고칠 수 있게 빌린다"
fn add_world(text: &mut String) {
    text.push_str(" world!"); // 빌린 문자열 뒤에 이어붙이기
}
