// ============================================================
// 01 · Hello World — 첫 Rust 프로그램
// ------------------------------------------------------------
// [실행] rust_class 폴더에서:
//   cargo run --bin 01_hello_world
//
// [핵심] Rust 프로그램은 fn main() 에서 시작한다.
//   Rust 는 '컴파일 언어' — cargo run 이 (1) 컴파일 → (2) 실행 을 한 번에 해준다.
//   (Python/JS 는 인터프리터가 즉석 실행. Rust 는 기계어로 번역한 뒤 돈다.)
// ============================================================

fn main() {
    // println! 끝의 ! 는 '매크로'라는 뜻 (그냥 함수가 아님).
    //   지금은 "출력은 println! 로 한다" 정도로만 기억하면 된다.
    println!("Hello, world!"); // → Hello, world!

    // [JS/Python 비교]
    //   JS:     console.log("...")
    //   Python: print("...")
    //   Rust:   println!("...");   ← ; 세미콜론으로 문장을 끝낸다 (JS 처럼)

    // {} 는 자리표시자(placeholder). 값이 그 자리에 끼워진다.
    //   Python f-string, JS 템플릿 리터럴과 목적은 같지만 문법이 다르다.
    let name = "루크";
    println!("안녕하세요, {}님!", name); // → 안녕하세요, 루크님!

    // Rust 1.58+ 는 변수명을 {} 안에 바로 넣는 것도 된다 (더 f-string 스러움).
    println!("이름은 {name} 입니다."); // → 이름은 루크 입니다.

    // 숫자도 똑같이 {} 로 출력
    let year = 2026;
    println!("올해는 {year}년"); // → 올해는 2026년

    // 여러 값 한 줄에 — {} 순서대로 끼워짐
    println!("{}년 {}월", 2026, 7); // → 2026년 7월
}
