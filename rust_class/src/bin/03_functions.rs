// ============================================================
// 03 · 함수 — 타입을 반드시 쓰고, 마지막 '식'이 반환값
// ------------------------------------------------------------
// 실행: cargo run --bin 03_functions
//
// [핵심 2가지]
//   1) 매개변수와 반환값에 타입을 '반드시' 적는다 (Python/JS 는 선택).
//   2) 함수 마지막 줄에 세미콜론(;)을 빼면 그게 반환값이 된다 (return 생략 가능).
// ============================================================

fn main() {
    let sum = add(3, 4);
    println!("3 + 4 = {sum}"); // → 3 + 4 = 7

    greet("루크"); // → 안녕하세요, 루크님!

    let bigger = max(10, 7);
    println!("더 큰 값 = {bigger}"); // → 더 큰 값 = 10
}

// i32 두 개를 받아 i32 를 반환.  -> i32 가 '반환 타입' 표기.
fn add(a: i32, b: i32) -> i32 {
    a + b // ← 세미콜론 없음! 이 '식(expression)'의 값이 곧 반환값.
          //   a + b; 처럼 ; 를 붙이면 '문(statement)'이 되어 반환값이 사라져 에러난다.
          //   (return a + b; 처럼 명시적 return 도 되지만, Rust 는 위 방식을 선호)
}

// 반환값이 없는 함수는 -> 를 안 쓴다.
fn greet(name: &str) {
    // &str = "문자열 슬라이스" 타입. 지금은 "문자열을 받는다" 정도로 이해하면 된다.
    //   (04 소유권에서 왜 & 가 붙는지 자세히 다룬다)
    println!("안녕하세요, {name}님!");
}

// if 도 '식'이라 값을 돌려준다 → JS 삼항연산자 자리를 대신한다.
fn max(a: i32, b: i32) -> i32 {
    if a > b {
        a // 세미콜론 없음 = 이 블록이 내놓는 값
    } else {
        b
    }
    // [JS 비교] return a > b ? a : b;  와 같은 일을 Rust 는 'if 식'으로 한다.
}
